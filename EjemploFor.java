public class EjemploFor {
    public static void main(String[] args) {
        
        // Todo se define dentro del paréntesis: (Inicio; Condición; Avance)
        for (int contadorNumero = 1; contadorNumero <= 5; contadorNumero++) {
            System.out.println("Contando con for: " + contadorNumero);
        }
        
    }
}